def parse_llm_response(data: dict) -> list[ConceptWithTypes]:
    rows = data.get("concepts") or data.get("rows") or []
    out: list[ConceptWithTypes] = []
    for raw in rows:
        if not isinstance(raw, dict):
            continue
        c = _parse_concept(raw)
        if c.concept_title:
            out.append(c)
    return out


