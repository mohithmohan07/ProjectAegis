def _parse_case(raw: dict) -> MinedCase:
    return MinedCase(
        case_id=str(raw.get("case_id") or ""),
        source_question_id=str(raw.get("source_question_id") or ""),
        case_prompt=str(raw.get("case_prompt") or ""),
        case_signature=str(raw.get("case_signature") or ""),
    )


def _parse_type(raw: dict) -> MinedType:
    cases_raw = raw.get("cases") or raw.get("case_prompts") or []
    return MinedType(
        type_id=str(raw.get("type_id") or ""),
        type_title=str(raw.get("type_title") or ""),
        type_description=str(raw.get("type_description") or ""),
        task_pattern=str(raw.get("task_pattern") or ""),
        topic_hint=str(raw.get("topic_hint") or ""),
        concept_match_hint=str(raw.get("concept_match_hint") or ""),
        source_question_ids=[str(x) for x in (raw.get("source_question_ids") or [])],
        cases=[_parse_case(c) for c in cases_raw if isinstance(c, dict)],
    )


def _parse_concept(raw: dict) -> ConceptWithTypes:
    kws = raw.get("keywords") or []
    if isinstance(kws, str):
        kws = [k.strip() for k in kws.split(",") if k.strip()]
    evidence = raw.get("source_evidence") or []
    if isinstance(evidence, str):
        evidence = [evidence] if evidence.strip() else []
    return ConceptWithTypes(
        concept_id=str(raw.get("concept_id") or ""),
        topic=str(raw.get("topic") or ""),
        parent_concept=str(raw.get("parent_concept") or ""),
        concept_title=str(raw.get("concept_title") or raw.get("concept") or ""),
        description_body=str(raw.get("description_body") or ""),
        mastery=str(raw.get("mastery") or ""),
        misconception=(str(raw.get("misconception")).strip() or None)
        if raw.get("misconception") else None,
        keywords=[str(k) for k in kws],
        source_evidence=[str(e) for e in evidence],
        types=[_parse_type(t) for t in (raw.get("types") or []) if isinstance(t, dict)],
    )


def _concept_to_dict(c: ConceptWithTypes) -> dict:
    return {
        "concept_id": c.concept_id,
        "topic": c.topic,
        "parent_concept": c.parent_concept,
        "concept_title": c.concept_title,
        "description_body": c.description_body,
        "mastery": c.mastery,
        "misconception": c.misconception or "",
        "keywords": c.keywords,
        "source_evidence": c.source_evidence,
        "types": [
            {
                "type_id": t.type_id,
                "type_title": t.type_title,
                "type_description": t.type_description,
                "task_pattern": t.task_pattern,
                "topic_hint": t.topic_hint,
                "concept_match_hint": t.concept_match_hint,
                "source_question_ids": t.source_question_ids,
                "cases": [
                    {
                        "case_id": cs.case_id,
                        "source_question_id": cs.source_question_id,
                        "case_prompt": cs.case_prompt,
                        "case_signature": cs.case_signature,
                    }
                    for cs in t.cases
                ],
            }
            for t in c.types
        ],
    }


def parse_llm_response(data: dict) -> list[ConceptWithTypes]:
    rows = data.get("concepts") or data.get("rows") or []
    out: list[ConceptWithTypes] = []
    for raw in rows:
        if not isinstance(raw, dict):
            continue
        c = _parse_concept(raw)
        if c.concept_title:
            out.append(c)
    return out


def build_output_rows(
    cfg: RunConfig, concepts: list[ConceptWithTypes],
) -> tuple[dict, list[dict]]:
    """Return (meta, workbook-oriented rows)."""
    topics = canonical_topics(cfg)
    by_topic: dict[str, list[ConceptCore]] = {}
    for c in concepts:
        t = topic_display_name(c.topic)
        by_topic.setdefault(t, []).append(c)

    rows: list[dict] = []
    for concept in concepts:
        topic = topic_display_name(concept.topic)
        rows.append({
            "chapter_tag": chapter_tag(cfg),
            "chapter_duration_minutes": cfg.chapter_duration_minutes,
            "topic_name": topic,
            "topic_display_name": topic,
            "topic_tag": topic_tag(cfg, topic, "PL"),
            "parent_concept": concept.parent_concept,
            "concept_title": concept.concept_title,
            "concept_display_name": concept.concept_title,
            "concept_tag": concept_tag(cfg, concept, "PL"),
            "concept_description": render_concept_description(concept),
            "keywords": ", ".join(
                k.lower().strip() for k in concept.keywords if k.strip()),
            "topic_concept_labels": topic_concept_labels(by_topic.get(topic, [])),
        })
    meta = {
        "chapter_tag": chapter_tag(cfg),
        "chapter_duration_minutes": cfg.chapter_duration_minutes,
        "topics": topics,
        "topic_descriptions": {},
        "chapter_description": "",
    }
    return meta, rows


def to_legacy_records(rows: list[dict]) -> list[dict]:
    """Convert V2 output rows to the legacy deposit schema."""
    out: list[dict] = []
    for row in rows:
        title = norm(row.get("concept_title", ""))
        if not title:
            continue
        out.append({
            "topic": row.get("topic_name") or row.get("topic", "General"),
            "parent_concept": row.get("parent_concept", ""),
            "concept_title": title,
            "concept_details": row.get("concept_description", ""),
            "keywords": row.get("keywords", ""),
        })
    return out


def generate_concept_map_v2(
    cfg: RunConfig,
    chapter_text: str,
    inventory: list[dict],
    *,
    call_llm_json: Callable[[str, str], dict],
    log: Callable[[str, str], None] | None = None,
) -> tuple[dict, list[dict], list[ConceptWithTypes]]:
    """Run the V2 pipeline: master prompt → validate → optional repair → render."""
    def _log(msg: str, level: str = "info") -> None:
        if log:
            log(msg, level)

    assert_finalized_config(cfg)
    system = (
        "You are a strict source-grounded curriculum engine. "
        "Return ONLY valid JSON matching the requested schema."
    )
    prompt = build_concept_map_prompt(cfg, chapter_text, inventory)
    _log("Concept Map V2: running master prompt.")
    data = call_llm_json(system, prompt)
    concepts = parse_llm_response(data)
    if not concepts:
        raise RuntimeError("Concept Map V2: master prompt returned no concepts")

    concepts = sanitize_concept_cases(concepts, chapter_text=chapter_text)
    errors = validate_concept_map(cfg, concepts)
    if errors:
        _log(f"Concept Map V2: {len(errors)} validation error(s) — repair pass.", "warning")
        repair = build_repair_prompt(cfg, concepts, errors, chapter_text, inventory)
        data = call_llm_json(system, repair)
        concepts = parse_llm_response(data)
        concepts = sanitize_concept_cases(concepts, chapter_text=chapter_text)
        errors = validate_concept_map(cfg, concepts)

    if errors:
        raise RuntimeError(
            "Concept map validation failed:\n" + "\n".join(errors))

    meta, rows = build_output_rows(cfg, concepts)
    _log(f"Concept Map V2: {len(concepts)} concept(s) validated.", "success")
    return meta, rows, concepts
