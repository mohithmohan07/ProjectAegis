def _slim_inventory(items: list[dict]) -> list[dict]:
    """Pass only fields the V2 master prompt needs."""
    out: list[dict] = []
    for item in items:
        if not isinstance(item, dict):
            continue
        out.append({
            "qid": item.get("qid", ""),
            "source_kind": item.get("source_kind", "other"),
            "source_label": item.get("source_label", ""),
            "topic_hint": item.get("topic_hint", ""),
            "raw_task": item.get("raw_task", ""),
            "normalized_task": item.get("normalized_task", ""),
            "raw_solution_or_answer": item.get("raw_solution_or_answer", ""),
            "shared_context": item.get("shared_context", ""),
            "subpart_label": item.get("subpart_label", ""),
            "order_index": item.get("order_index", 0),
        })
    return out


def build_concept_map_prompt(
    cfg: RunConfig, chapter_text: str, inventory: list[dict],
) -> str:
    assert_finalized_config(cfg)
    topics = canonical_topics(cfg)
    template = (
        cfg.english_literature_template_concepts or DEFAULT_ENGLISH_LIT_TEMPLATE
    )
    import json
    inv_json = json.dumps(_slim_inventory(inventory), ensure_ascii=False, indent=2)
    topic_lines = "\n".join(f"  {i + 1}. {t}" for i, t in enumerate(topics))
    template_lines = "\n".join(f"  {i + 1}. {x}" for i, x in enumerate(template))

    return f"""
You are a strict source-grounded concept mapping engine for school curriculum.

Return ONLY strict JSON:
{{
  "concepts": [
    {{
      "concept_id": "CONCEPT-0001",
      "topic": "",
      "parent_concept": "",
      "concept_title": "",
      "description_body": "",
      "mastery": "",
      "misconception": "",
      "keywords": [],
      "source_evidence": [],
      "types": [
        {{
          "type_id": "TYPE-0001",
          "type_title": "",
          "type_description": "",
          "task_pattern": "",
          "topic_hint": "",
          "concept_match_hint": "",
          "source_question_ids": ["QINV-0001"],
          "cases": [
            {{
              "case_id": "CASE-0001",
              "source_question_id": "QINV-0001",
              "case_prompt": "",
              "case_signature": ""
            }}
          ]
        }}
      ]
    }}
  ],
  "audit": {{
    "chapter_tag": "{chapter_tag(cfg)}",
    "chapter_duration_minutes": {cfg.chapter_duration_minutes},
    "topic_count": {len(topics)},
    "coverage_notes": []
  }}
}}

NON-NEGOTIABLE RUN CONTEXT:
- Board: {cfg.board}
- Class: {cfg.class_num}
- Subject: {cfg.subject}
- Chapter: {cfg.chapter}
- Publication: {cfg.publication}
- Chapter tag must be exactly: {chapter_tag(cfg)}
- Chapter duration must be exactly: {cfg.chapter_duration_minutes}
- Allowed topics, in exact reading order:
{topic_lines}

TOPIC RULES:
- Use ONLY the allowed topics above.
- topic must exactly match one allowed topic.
- Do not create Overview, Introduction, Miscellaneous, Practice, Exercise, Examples, or Activity topics unless they are explicitly present in the allowed topic list.
- Do not duplicate a concept across two topics.
- BPT, CBPT, theorem variants, formulas, events, characters, processes, or definitions belong only to the topic where the source teaches them most directly.
- If a later topic reuses an earlier concept, do not create it again. Mention reuse only inside the relevant Type/Case when needed.

CONCEPT RULES:
- A concept is a durable teachable mastery objective.
- Do not create rows for cases, examples, individual questions, generic activities, or teacher methods.
- concept_title must be the title name only. No display prefix, no topic number, no chapter number.
- Use textbook/source vocabulary.
- Avoid repeated sibling openers.
- parent_concept must be a compact cluster name, not a duplicate of topic.
- keywords: 3 to 6 lowercase keywords.

DESCRIPTION RULES:
- description_body must contain only the teachable explanation.
- Do not include "Description:", "Types:", "Case:", "Misconception:", or "Achieving Mastery:" inside description_body.
- 45 to 90 words unless the concept is very simple.
- Keep the scope of the concept tight. Do not pull types/cases/details from the next concept.
- mastery must be one short sentence saying what the learner can do after mastering this concept.
- misconception must appear at most once and must be specific.
- Do not write generic misconceptions.
- Do not write N/A, None, Not applicable, filler, or placeholders.

TYPES AND CASES:
- Types classify assessable question/task varieties under that exact concept.
- Cases are sub-types/examples under Types.
- If no source-backed Case exists for a Type, do not list that Type.
- If a concept has no meaningful assessable source task, use an empty types array.
- Every Case must be copied from the source question/task inventory as a full teacher-executable prompt.
- Do not shorten questions.
- Preserve all numbers, expressions, diagrams/tables/passages context, names, dates, givens, conditions, quotations, and exact asks.
- Do not write "Example 3", "Exercise 1.2", "Fig 6.4", "Table 1", page references, or source labels in case_prompt.
- Substitute the actual content instead.
- Include as many source examples as available for each Type.
- Every inventory item must be assigned to exactly one concept unless it is truly rhetorical/non-assessable.
- Do not assign one question/task to multiple concepts.
- Culmination or mixed Types are allowed only when a source task combines multiple concepts from the same topic.

SUBJECT-SPECIFIC RULES:
- Mathematics: Types may be numerical, proof, construction, graph, theorem application, formula, diagram, reasoning, or word-problem patterns.
- Science: Types may be diagram, experiment, observation, inference, process, comparison, numerical, reasoning, or application patterns.
- Social Science: Types may be definition, cause-effect, comparison, chronology, map, source, case, data, evidence, or long-answer patterns.
- English Literature: topic must be exactly the prose/poem/drama name "{cfg.chapter}".
- English Literature must follow this concept family unless a source-specific idea clearly requires a tighter concept:
{template_lines}
- English Literature must NOT create pedagogy/activity concepts such as pre-reading prediction, oral checks, reading in parts, informal letter writing, family news, classroom discussion, or lesson planning unless the chapter itself assesses it as text content.

OUTPUT SAFETY:
- Return JSON only.
- No markdown.
- No commentary.
- No source-artifact labels.
- Never use the words MMD or MMDs.

QUESTION / TASK INVENTORY:
{inv_json}

CHAPTER SOURCE:
{chapter_text}
""".strip()


def build_repair_prompt(
    cfg: RunConfig,
    concepts: list[ConceptWithTypes],
    errors: list[str],
    chapter_text: str,
    inventory: list[dict],
) -> str:
    import json
    topics = canonical_topics(cfg)
    topic_lines = "\n".join(f"  {i + 1}. {t}" for i, t in enumerate(topics))
    err_lines = "\n".join(f"{i + 1}. {e}" for i, e in enumerate(errors))
    payload = {"concepts": [_concept_to_dict(c) for c in concepts]}
    inv_json = json.dumps(_slim_inventory(inventory), ensure_ascii=False, indent=2)
    return f"""
Repair the concept map JSON using ONLY the listed validation errors.

Return ONLY strict JSON:
{{ "concepts": [ ...same schema... ] }}

Rules:
- Fix only the errors listed.
- Preserve valid rows, valid topics, valid descriptions, valid mastery lines, and valid types.
- Do not rewrite the full chapter.
- Do not create new topics.
- Use only these allowed topics:
{topic_lines}
- Chapter duration remains exactly {cfg.chapter_duration_minutes}.
- Chapter tag remains exactly {chapter_tag(cfg)}.
- If a Case is too short or refers to Example/Exercise/Figure/Table/page, replace it with the full actual source question from the inventory/source.
- If a Type has no valid source-backed Case, remove that Type.
- If a misconception is duplicated or generic, keep only one specific misconception or remove it.
- If a concept appears in two topics, keep it only in the source topic where it is directly taught.
- If English Literature has pedagogy/activity concepts, replace them with source-text concepts following the literature template.

VALIDATION ERRORS:
{err_lines}

CURRENT CONCEPT MAP:
{json.dumps(payload, ensure_ascii=False, indent=2)}

QUESTION / TASK INVENTORY:
{inv_json}

CHAPTER SOURCE:
{chapter_text}
""".strip()


