def norm(s: str) -> str:
    return re.sub(r"\s+", " ", str(s or "")).strip()


def slug(s: str) -> str:
    return re.sub(r"^_+|_+$", "",
                  re.sub(r"[^A-Za-z0-9]+", "_",
                         norm(s).replace("'", "").replace("'", "")))


def strip_numbering(s: str) -> str:
    out = norm(s)
    out = re.sub(
        r"^(chapter|unit|topic|section|exercise|ex)\s*\d+([.\-:]\d+)*\s*[:.)-]?\s*",
        "", out, flags=re.IGNORECASE)
    out = re.sub(r"^\d+([.\-:]\d+)*\s*[:.)-]?\s*", "", out)
    return out.strip()


def subject_code(subject: str) -> str:
    key = subject.lower().strip()
    return SUBJECT_CODE.get(key) or slug(subject)[:2].upper()


def subject_display(subject: str) -> str:
    key = subject.lower().strip()
    return SUBJECT_DISPLAY.get(key) or slug(subject)


def board_code(board: str) -> str:
    return board.upper().strip()


def class_code(class_num: int) -> str:
    return str(class_num).zfill(2)


def concept_prefix(cfg: RunConfig) -> str:
    return f"{class_code(cfg.class_num)}{board_code(cfg.board)[:2]}{subject_code(cfg.subject)}"


def chapter_tag(cfg: RunConfig) -> str:
    return (
        f"{class_code(cfg.class_num)}_{subject_display(cfg.subject)}_"
        f"{board_code(cfg.board)}_{slug(cfg.publication)}"
    )


def topic_display_name(topic: str) -> str:
    return strip_numbering(topic)


def topic_tag(cfg: RunConfig, topic: str, stage: str = "PL") -> str:
    return f"{concept_prefix(cfg)}_{slug(cfg.chapter)}_{stage}_{slug(topic_display_name(topic))}"


def concept_tag(cfg: RunConfig, concept: ConceptCore, stage: str = "PL") -> str:
    return f"{concept_prefix(cfg)}_{slug(cfg.chapter)}_{stage}_{slug(concept.concept_title)}"


def topic_concept_labels(concepts: list[ConceptCore]) -> str:
    return ", ".join(c.concept_title for c in concepts)


def is_english_literature(cfg: RunConfig) -> bool:
    return cfg.subject.lower().strip() in {"english", "english literature"}


def assert_finalized_config(cfg: RunConfig) -> None:
    if not cfg.publication:
        raise ValueError(
            "Missing publication. Chapter tag must include publication.")
    if not cfg.chapter_duration_minutes or cfg.chapter_duration_minutes <= 0:
        raise ValueError(
            "Missing finalized chapter duration. Do not let the LLM estimate this.")
    if not cfg.expected_topics:
        raise ValueError(
            "Missing expected_topics. Lock topics from textbook TOC/source "
            "headings before concept extraction.")


def canonical_topics(cfg: RunConfig) -> list[str]:
    topics = [topic_display_name(t) for t in cfg.expected_topics if topic_display_name(t)]
    if not cfg.allow_introduction_topic:
        topics = [t for t in topics if not re.match(r"^(introduction|overview)$", t, re.I)]
    if is_english_literature(cfg):
        topics = [cfg.chapter]
    seen: set[str] = set()
    out: list[str] = []
    for t in topics:
        key = t.lower()
        if key not in seen:
            seen.add(key)
            out.append(t)
    return out


def parse_duration_minutes(text: str) -> int | None:
    m = re.search(r"(\d+)", text or "")
    return int(m.group(1)) if m else None


def run_config_from_meta(
    *,
    board: str = "",
    grade: str = "",
    subject: str = "",
    chapter_title: str = "",
    publication: str = "",
    chapter_duration: str = "",
    expected_topics: list[str],
    allow_introduction_topic: bool | None = None,
) -> RunConfig:
    """Build a :class:`RunConfig` from chapter/upload metadata."""
    class_num = 10
    m = re.search(r"\d+", grade or "")
    if m:
        class_num = int(m.group())
    duration = parse_duration_minutes(chapter_duration)
    if not duration:
        # Deterministic fallback: ~60 minutes per locked topic, bounded.
        n = max(len(expected_topics), 1)
        duration = max(180, min(720, n * 60))
    if allow_introduction_topic is None:
        allow_introduction_topic = any(
            re.match(r"^introduction$", topic_display_name(t), re.I)
            for t in expected_topics
        )
    pub = norm(publication) or "NCERT"
    return RunConfig(
        board=board or "CBSE",
        class_num=class_num,
        subject=subject or "General",
        chapter=chapter_title or "Chapter",
        publication=pub,
        chapter_duration_minutes=duration,
        expected_topics=expected_topics,
        allow_introduction_topic=allow_introduction_topic,
    )


def has_source_artifact(text: str) -> bool:
    return bool(_SOURCE_ARTIFACT_RE.search(text or ""))


def sanitize_case_prompt(prompt: str, *, chapter_text: str = "") -> str:
    """Rewrite source-artifact labels; inline figure images from chapter MMD."""
    p = norm(prompt)
    if not p:
        return p
    fig_index = figure_markdown_index_from_mmd(chapter_text)
    p = inline_figures_in_text(p, fig_index)
    p = neutralize_source_artifacts_preserve_images(p)
    return clean_one_line(p)


def sanitize_concept_cases(
    concepts: list[ConceptWithTypes],
    *,
    chapter_text: str = "",
) -> list[ConceptWithTypes]:
    """Deterministically clean all case prompts before validation."""
    for concept in concepts:
        for type_ in concept.types:
            for case in type_.cases:
                case.case_prompt = sanitize_case_prompt(
                    case.case_prompt, chapter_text=chapter_text,
                )
    return concepts


def clean_one_line(s: str) -> str:
    out = norm(s).replace("MMDs", "chapter").replace("MMD", "chapter")
    # Protect URLs before normalizing section separators (avoid breaking https://).
    urls: list[str] = []

    def _stash_url(match: re.Match[str]) -> str:
        urls.append(match.group(0))
        return f"__URL_{len(urls) - 1}__"

    out = re.sub(r"https?://\S+", _stash_url, out)
    out = re.sub(r"\s*//\s*", " // ", out)
    for i, url in enumerate(urls):
        out = out.replace(f"__URL_{i}__", url)
    return out.strip()


def sanitize_description_body(s: str) -> str:
    """Strip pre-rendered section labels so rendering never duplicates blocks."""
    out = str(s or "")
    out = re.sub(r"^Description:\s*", "", out, flags=re.I)
    out = re.sub(r"\s*//\s*Types:\s*.*$", "", out, flags=re.I | re.S)
    out = re.sub(r"\s*//\s*Misconceptions?:\s*.*$", "", out, flags=re.I | re.S)
    out = re.sub(r"\n?Achieving Mastery:\s*.*$", "", out, flags=re.I | re.S)
    out = re.sub(r"\s*//\s*Achieving Mastery:\s*.*$", "", out, flags=re.I | re.S)
    out = clean_one_line(out.strip())
    if not out:
        raise ValueError("Empty description_body after sanitization.")
    return out


def sanitize_mastery(s: str) -> str:
    out = re.sub(r"^Achieving Mastery:\s*", "", str(s or ""), flags=re.I).strip()
    out = clean_one_line(out)
    if not out:
        raise ValueError("Missing Achieving Mastery line.")
    return re.sub(r"[.]*$", ".", out)


def sanitize_misconception(s: str | None) -> str | None:
    if not s:
        return None
    out = re.sub(r"^Misconceptions?:\s*", "", str(s), flags=re.I)
    out = re.sub(r"\b(n/a|none|not applicable)\b", "", out, flags=re.I).strip()
    out = clean_one_line(out)
    if not out:
        return None
    if any(bad in out.lower() for bad in _GENERIC_MISCONCEPTIONS):
        return None
    return re.sub(r"[.]*$", ".", out)


def case_prompt_is_valid(prompt: str) -> bool:
    p = norm(prompt)
    if not p or len(p) < 20:
        return False
    # Alt text in inlined figure images may retain Fig. N labels — ignore those.
    stripped = re.sub(r"!\[[^\]]*\]\([^)]+\)", "[figure]", p)
    return not has_source_artifact(stripped)


def render_types(types: list[MinedType]) -> str:
    rendered: list[str] = []
    for type_ in types:
        valid_cases = [c for c in type_.cases if case_prompt_is_valid(c.case_prompt)]
        if not valid_cases:
            continue
        case_text = " ".join(
            f"Case {str(i + 1).zfill(2)}: {clean_one_line(c.case_prompt)}"
            for i, c in enumerate(valid_cases)
        )
        rendered.append(
            f"Type {str(len(rendered) + 1).zfill(2)}: "
            f"{clean_one_line(type_.type_title)} {case_text}"
        )
    return " ".join(rendered)


def render_concept_description(concept: ConceptWithTypes) -> str:
    body = sanitize_description_body(concept.description_body)
    mastery = sanitize_mastery(concept.mastery)
    misconception = sanitize_misconception(concept.misconception)
    types_text = render_types(concept.types or [])
    sections = [
        f"Description: {body}\nAchieving Mastery: {mastery}",
        f"Misconception: {misconception}" if misconception else None,
        f"Types: {types_text}" if types_text else None,
    ]
    return " // ".join(s for s in sections if s)


