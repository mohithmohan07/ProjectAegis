"""Concept Map V2 — source-locked pipeline, prompt, validation, and renderer.

Replaces the multi-pass staged concept mapper with one master LLM call (+ optional
repair) and deterministic post-processing:

1. Topics locked to textbook/source headings (``expected_topics``).
2. Chapter tags and subject prefixes generated by code, not the model.
3. ``topic_display_name`` is exactly the topic name.
4. ``topic_concept_labels`` uses concept title, not display name.
5. Misconception appears once only.
6. Types/Cases structured first, then rendered into ``concept_description`` once.
7. Cases are sub-types under Types and must carry full source questions.
8. Examples/questions copied from source inventory — never shortened.
9. Chapter duration from finalized config, not the model.
10. English Literature uses the literature template and rejects pedagogy concepts.
"""
