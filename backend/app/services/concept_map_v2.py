def validate_concept_map(cfg: RunConfig, concepts: list[ConceptWithTypes]) -> list[str]:
    errors: list[str] = []
    topics = canonical_topics(cfg)
    allowed = {t.lower() for t in topics}
    seen_concepts: dict[str, str] = {}
    seen_type_assignments: dict[str, str] = {}
    seen_source_questions: dict[str, str] = {}

    for c in concepts:
        topic = topic_display_name(c.topic)
        if topic.lower() not in allowed:
            errors.append(
                f'Invalid topic "{c.topic}". Must be one of: {" | ".join(topics)}')
        if (re.match(r"^(overview|introduction)$", topic, re.I)
                and not cfg.allow_introduction_topic
                and not is_english_literature(cfg)):
            errors.append(
                f'Unwanted topic "{topic}". Do not create Overview/Introduction '
                "unless source requires it.")

        if is_english_literature(cfg):
            title_l = c.concept_title.lower()
            bad = next((t for t in ENGLISH_LIT_BANNED_CONCEPT_TERMS if t in title_l), None)
            if bad:
                errors.append(
                    f'English Literature concept "{c.concept_title}" is invalid '
                    f"pedagogy/activity content: {bad}")

        key = slug(c.concept_title).lower()
        if key in seen_concepts:
            errors.append(
                f'Duplicate concept "{c.concept_title}" appears in both '
                f'"{seen_concepts[key]}" and "{topic}".')
        else:
            seen_concepts[key] = topic

        if len(re.findall(r"Misconceptions?:", c.misconception or "", re.I)) > 1:
            errors.append(f'Duplicate Misconception section in "{c.concept_title}".')
        if c.misconception and not sanitize_misconception(c.misconception):
            errors.append(f'Generic or invalid misconception in "{c.concept_title}".')

        for t in c.types or []:
            if not t.cases:
                errors.append(
                    f'Type "{t.type_title}" under "{c.concept_title}" has no '
                    "source-backed Cases.")
            if t.type_id in seen_type_assignments:
                errors.append(
                    f"Type {t.type_id} assigned to more than one concept: "
                    f'"{seen_type_assignments[t.type_id]}" and "{c.concept_title}".')
            else:
                seen_type_assignments[t.type_id] = c.concept_title
            for qid in t.source_question_ids or []:
                if qid in seen_source_questions:
                    errors.append(
                        f"Question {qid} assigned to multiple concepts: "
                        f'"{seen_source_questions[qid]}" and "{c.concept_title}".')
                else:
                    seen_source_questions[qid] = c.concept_title
            for cs in t.cases or []:
                if not case_prompt_is_valid(cs.case_prompt):
                    errors.append(
                        f'Invalid/short/source-artifact Case in "{c.concept_title}" > '
                        f'"{t.type_title}": "{cs.case_prompt}"')
    return errors


